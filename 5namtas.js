function printNamtas6(range) {
      for (var i = 1; i <= range; i++) {
        var result = 6 * i;
        console.log("6 x " + i + " = " + result);
      }
    }

    // Change the range as needed
    var tableRange = 10;

    // Print Namtas 6 up to the specified range
    printNamtas6(tableRange);
